package basics;

public class Switch {
    public static void main(String[] args) {
        int age=7;
        switch(age) {
            case 1:
                System.out.println("1");
            case 2:
                System.out.println("2");
            case 3:
                System.out.println("10");
            case 4:
                System.out.println("10");
            case 5:
                System.out.println("10");
                break;
            default:
                System.out.println("Default Statement");

        }
    }
}
